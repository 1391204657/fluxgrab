  function langFromUrl() {
    var m = location.search.match(/[?&]lang=([^&]+)/);
    if (!m) return null;
    var code = decodeURIComponent(m[1]);
    return T[code] ? code : null;
  }

  function syncUrlLang(lang) {
    if (!window.history || !window.history.replaceState) return;
    try {
      var u = new URL(location.href);
      if (lang && lang !== "en") u.searchParams.set("lang", lang);
      else u.searchParams.delete("lang");
      var next = u.pathname + u.search + u.hash;
      var cur = location.pathname + location.search + location.hash;
      if (next !== cur) history.replaceState(null, "", next);
    } catch (e) { /* ignore */ }
  }

  function guessLang() {
    var n = (navigator.language || "en").toLowerCase();
    if (n.startsWith("zh-tw") || n.startsWith("zh-hk")) return "zh-TW";
    if (n.startsWith("zh")) return "zh";
    if (n.startsWith("es")) return "es";
    if (n.startsWith("ja")) return "ja";
    if (n.startsWith("de")) return "de";
    if (n.startsWith("fr")) return "fr";
    if (n.startsWith("pt")) return "pt";
    return "en";
  }

  function t(lang, key, vars) {
    var pack = T[lang] || T.en;
    var s = pack[key] || T.en[key] || key;
    if (vars) {
      Object.keys(vars).forEach(function (k) {
        s = s.replace(new RegExp("\\{" + k + "\\}", "g"), vars[k]);
      });
    }
    return s;
  }

  function getLang() {
    var saved = localStorage.getItem(STORE);
    return saved && T[saved] ? saved : guessLang();
  }

  function tf(key, vars) {
    return t(getLang(), key, vars);
  }

  function htmlLang(lang) {
    if (lang === "zh") return "zh-CN";
    if (lang === "zh-TW") return "zh-TW";
    return lang;
  }

  function apply(lang) {
    if (!T[lang]) lang = "en";
    localStorage.setItem(STORE, lang);
    document.documentElement.lang = htmlLang(lang);
    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      el.textContent = t(lang, el.getAttribute("data-i18n"));
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach(function (el) {
      el.placeholder = t(lang, el.getAttribute("data-i18n-placeholder"));
    });
    document.querySelectorAll("[data-i18n-html]").forEach(function (el) {
      el.innerHTML = t(lang, el.getAttribute("data-i18n-html"));
    });
    var titleEl = document.querySelector("title[data-i18n]");
    if (titleEl) document.title = t(lang, titleEl.getAttribute("data-i18n"));
    syncUrlLang(lang);
    if (window.FluxGrabSEO && document.body.classList.contains("page-home-tool")) {
      FluxGrabSEO.applyHomeMeta(lang);
      FluxGrabSEO.syncUrlLang(lang);
    }
    updateActiveLang(lang);
    document.dispatchEvent(new CustomEvent("fluxgrab:lang", { detail: { lang: lang } }));
  }

  function updateActiveLang(lang) {
    document.querySelectorAll(".lang-menu-item").forEach(function (btn) {
      btn.classList.toggle("active", btn.getAttribute("data-lang") === lang);
      btn.setAttribute("aria-selected", btn.getAttribute("data-lang") === lang ? "true" : "false");
    });
  }

  function closeLangMenu() {
    var wrap = document.querySelector(".lang-icon-wrap");
    var btn = document.getElementById("langBtn");
    if (wrap) wrap.classList.remove("open");
    if (btn) btn.setAttribute("aria-expanded", "false");
  }

  function buildLangMenu() {
    var menu = document.getElementById("langMenu");
    if (!menu) return;
    menu.innerHTML = "";
    Object.keys(LANGS).forEach(function (code) {
      var li = document.createElement("li");
      var btn = document.createElement("button");
      btn.type = "button";
      btn.className = "lang-menu-item";
      btn.setAttribute("data-lang", code);
      btn.setAttribute("role", "option");
      btn.textContent = LANGS[code];
      btn.addEventListener("click", function (e) {
        e.stopPropagation();
        apply(code);
        closeLangMenu();
      });
      li.appendChild(btn);
      menu.appendChild(li);
    });
  }

  function buildSelect() {
    buildLangMenu();
  }

  function init() {
    buildSelect();
    var fromUrl = langFromUrl();
    var saved = localStorage.getItem(STORE);
    var lang = fromUrl || (saved && T[saved] ? saved : guessLang());
    apply(lang);

    var btn = document.getElementById("langBtn");
    var wrap = btn && btn.closest(".lang-icon-wrap");
    if (btn && wrap) {
      btn.addEventListener("click", function (e) {
        e.stopPropagation();
        var open = !wrap.classList.contains("open");
        wrap.classList.toggle("open", open);
        btn.setAttribute("aria-expanded", open ? "true" : "false");
      });
      document.addEventListener("click", closeLangMenu);
    }
  }

  return { init: init, apply: apply, t: tf, getLang: getLang, LANGS: LANGS };
